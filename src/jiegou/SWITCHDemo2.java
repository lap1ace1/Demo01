package jiegou;

public class SWITCHDemo2 {
    public static void main(String[] args) {
        String name = "lhl";
        switch (name){
            case "dsa":
                System.out.println("dsa");
                break;
            case "lhl":
                System.out.println("lhl");
                break;
            case "das":
                System.out.println("das");
                break;
            case "d":
                System.out.println("d");
                break;
            default:
                System.out.println("æ— ");
        }
    }
}
